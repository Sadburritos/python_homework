num = input("Введите число от 0 до 9: ")

if num == '1':
    print('!')
elif num == '2':
    print('@')
elif num == '3':
    print('#')
elif num == '4':
    print('$')
elif num == '5':
    print('%')
elif num == '6':
    print('^')
elif num == '7':
    print('&')
elif num == '8':
    print('*')
elif num == '9':
    print('(')
elif num == '0':
    print(')')
else:
    print("Это не число от 0 до 9")
